alunos=["Adriana", "Joao", "Maria", "Pedro", "Ana", "Lucas", "Mariana", "Adriana"]
print(f"Lista de alunos: {alunos}")
# Metodo remove()
alunos.remove("Adriana")
print(f"Lista de alunos: {alunos}")
# Metodo pop()
alunos.pop(2)
print(f"Lista de alunos: {alunos}")
# Funcao del
del alunos[3]
print(f"Lista de alunos: {alunos}")